#!/usr/bin/env python3
"""Cross-workbook composition visualizer for clay-workbench.

Reads every templates/library/*/template.json and emits:
  - docs/composition/library-graph.md         (cross-template flow)
  - docs/composition/per-template/<slug>.md   (per-template column DAG)

Pure stdlib. Idempotent. Use --check for CI drift detection.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
from pathlib import Path
from typing import Dict, Iterable, List, Set, Tuple

REPO_ROOT = Path(__file__).resolve().parent.parent
LIBRARY_DIR = REPO_ROOT / "templates" / "library"
DOCS_DIR = REPO_ROOT / "docs" / "composition"
PER_TEMPLATE_DIR = DOCS_DIR / "per-template"

AUTOGEN_BANNER = (
    "<!-- AUTO-GENERATED by scripts/compose-graph.py — do not edit by hand -->"
)

# Hint patterns that map a free-text phrase to one or more downstream slugs.
# Keys are lowercased substrings; values are lists of slug names that the
# matching template likely feeds INTO (when the phrase appears in this
# template's description or notes).
DOWNSTREAM_HINTS: Dict[str, List[str]] = {
    "/clay-outbound": ["outbound-3-step-cadence-cold", "outbound-3-step-cadence-warm"],
    "clay-outbound": ["outbound-3-step-cadence-cold", "outbound-3-step-cadence-warm"],
    "outbound cadence": ["outbound-3-step-cadence-cold", "outbound-3-step-cadence-warm"],
    "ready to hand to": ["outbound-3-step-cadence-cold", "outbound-3-step-cadence-warm"],
}

# Hint patterns that map a free-text phrase to one or more UPSTREAM slugs
# (templates that feed INTO this one when the phrase appears here).
# Self-edges are suppressed at insertion time (a template can't be its own
# upstream), which prevents the EU<->US-SMB waterfall cycle from forming off
# the generic "email waterfall" phrase.
UPSTREAM_HINTS: Dict[str, List[str]] = {
    "upstream email waterfall": ["email-waterfall-us-smb", "email-waterfall-eu"],
    "email waterfall": ["email-waterfall-us-smb", "email-waterfall-eu"],
    "signal monitor": ["signal-monitor-job-change", "signal-monitor-hiring-posture"],
    "signal-monitor": ["signal-monitor-job-change", "signal-monitor-hiring-posture"],
    "upstream signal monitor": ["signal-monitor-job-change", "signal-monitor-hiring-posture"],
    "from upstream account workbook": ["abm-account-keyed-tier-1"],
    "upstream account workbook": ["abm-account-keyed-tier-1"],
    "account-keyed source": ["abm-account-keyed-tier-1"],
}

# Sibling families — templates inside the same family are alternatives, not
# pipeline steps. Edges WITHIN a family are suppressed (prevents EU<->US-SMB
# cycle from the generic "email waterfall" phrase, and prevents
# cold->warm edge from "variant of <other cadence>" language).
SIBLING_FAMILIES: List[Set[str]] = [
    {"email-waterfall-us-smb", "email-waterfall-eu"},
    {"outbound-3-step-cadence-cold", "outbound-3-step-cadence-warm"},
    {"signal-monitor-job-change", "signal-monitor-hiring-posture"},
]


def _same_family(a: str, b: str) -> bool:
    for fam in SIBLING_FAMILIES:
        if a in fam and b in fam:
            return True
    return False


# Hard-coded canonical edges that aren't reliably inferable from text alone.
# Format: (from_slug, to_slug, label). Only added if both slugs exist.
# These cover the "T1 fan-out" — consumers of abm-account-keyed-tier-1's
# Tier-1 output that don't name it by slug because they predate it or are
# sibling templates in the same library family.
SEEDED_EDGES: List[Tuple[str, str, str]] = [
    (
        "abm-account-keyed-tier-1",
        "account-research-tier-1-brief",
        "T1 accounts -> tier-1 research brief",
    ),
    (
        "abm-account-keyed-tier-1",
        "prospect-research-champion-brief",
        "T1 accounts -> prospect-level dossier",
    ),
    (
        "abm-account-keyed-tier-1",
        "email-waterfall-eu",
        "T1 accounts (EU) -> email enrichment",
    ),
]


def load_templates() -> Dict[str, dict]:
    """Return {slug: parsed_json} for every template in the library."""
    out: Dict[str, dict] = {}
    for tpath in sorted(LIBRARY_DIR.glob("*/template.json")):
        with tpath.open() as fh:
            data = json.load(fh)
        slug = data["template"]["slug"]
        out[slug] = data
    return out


def collect_text_blob(tpl: dict) -> str:
    """Concatenate everything a human would read for cross-template hints."""
    parts: List[str] = []
    t = tpl.get("template", {})
    parts.append(t.get("description", "") or "")
    for col in tpl.get("columns", []):
        for k in ("notes", "description"):
            v = col.get(k)
            if v:
                parts.append(str(v))
    for act in tpl.get("actions", []):
        for k in ("notes", "description"):
            v = act.get(k)
            if v:
                parts.append(str(v))
    return "\n".join(parts)


def infer_cross_template_edges(
    templates: Dict[str, dict],
) -> List[Tuple[str, str, str]]:
    """Return list of (from_slug, to_slug, label) edges.

    Edge inference rules (in order, dedup by (from, to)):
      1. Direct slug match: if template B's blob contains slug A, then A -> B.
         Label is a short snippet from B's blob describing what's passed.
      2. Upstream hints: if template B's blob contains a known upstream phrase,
         add edges hint_slug -> B for each slug the phrase implies (filtered to
         slugs that actually exist).
      3. Downstream hints: if template A's blob contains a known downstream
         phrase, add edges A -> hint_slug for each slug the phrase implies.
    """
    slugs = list(templates.keys())
    edges: Dict[Tuple[str, str], str] = {}

    def add(from_s: str, to_s: str, label: str) -> None:
        if from_s == to_s or from_s not in templates or to_s not in templates:
            return
        if _same_family(from_s, to_s):
            return
        key = (from_s, to_s)
        # Keep the first non-empty label we see (stable order).
        if key not in edges:
            edges[key] = label

    # 1) Direct slug match.
    for to_slug, tpl in templates.items():
        blob = collect_text_blob(tpl).lower()
        for from_slug in slugs:
            if from_slug == to_slug:
                continue
            if from_slug.lower() in blob:
                add(from_slug, to_slug, snippet_for(tpl, from_slug))

    # 2) Upstream hints (this template names an upstream class).
    for to_slug, tpl in templates.items():
        blob_lc = collect_text_blob(tpl).lower()
        for phrase, hint_slugs in UPSTREAM_HINTS.items():
            if phrase in blob_lc:
                for s in hint_slugs:
                    add(s, to_slug, f"{phrase} -> {to_slug}")

    # 3) Downstream hints (this template hands off to a known class).
    for from_slug, tpl in templates.items():
        blob_lc = collect_text_blob(tpl).lower()
        for phrase, hint_slugs in DOWNSTREAM_HINTS.items():
            if phrase in blob_lc:
                for s in hint_slugs:
                    add(from_slug, s, f"{from_slug} -> {phrase}")

    # 4) Seeded canonical edges (T1 account fan-out etc).
    for from_s, to_s, lbl in SEEDED_EDGES:
        add(from_s, to_s, lbl)

    # Return deterministically sorted by (from, to).
    return sorted(((f, t, lbl) for (f, t), lbl in edges.items()))


def snippet_for(tpl: dict, target_slug: str) -> str:
    """Pull a short label describing why this edge exists, for the Mermaid arrow."""
    desc = (tpl.get("template", {}).get("description", "") or "")
    # Try to grab the sentence that mentions the slug.
    for sentence in re.split(r"(?<=[.!?])\s+", desc):
        if target_slug.lower() in sentence.lower():
            return shorten(sentence)
    # Fallback: scan notes.
    for col in tpl.get("columns", []):
        n = col.get("notes") or ""
        if target_slug.lower() in n.lower():
            return shorten(n)
    return f"feeds {tpl['template']['slug']}"


def shorten(s: str, n: int = 60) -> str:
    s = re.sub(r"\s+", " ", s).strip().strip(".")
    if len(s) <= n:
        return s
    return s[: n - 1].rstrip() + "…"


def build_column_dag(tpl: dict) -> List[Tuple[str, str]]:
    """Return list of (from_col, to_col) edges from depends_on."""
    edges: List[Tuple[str, str]] = []
    for col in tpl.get("columns", []):
        to = col["name"]
        for dep in col.get("depends_on") or []:
            edges.append((dep, to))
    for act in tpl.get("actions", []):
        # Actions don't have depends_on, but they often reference a
        # message_column / content_column / column triggering them.
        cfg = act.get("config") or {}
        ref_keys = ("message_column", "content_column", "column", "trigger_column")
        for k in ref_keys:
            v = cfg.get(k)
            if isinstance(v, str) and v:
                edges.append((v, f"[action] {act['name']}"))
        # Also: actions gated by run_condition often reference a formula col.
        rc = act.get("run_condition")
        if rc:
            for col in tpl.get("columns", []):
                cname = col["name"]
                if re.search(rf"\b{re.escape(cname)}\b", rc):
                    edges.append((cname, f"[action] {act['name']}"))
    # Dedup preserving order.
    seen: Set[Tuple[str, str]] = set()
    out: List[Tuple[str, str]] = []
    for e in edges:
        if e not in seen:
            seen.add(e)
            out.append(e)
    return out


# ---------- Rendering ----------


def mermaid_id(name: str) -> str:
    """Sanitize a node name for Mermaid."""
    return re.sub(r"[^A-Za-z0-9_]", "_", name)


def render_library_graph(
    templates: Dict[str, dict], edges: List[Tuple[str, str, str]]
) -> str:
    lines: List[str] = []
    lines.append(AUTOGEN_BANNER)
    lines.append("")
    lines.append("# Cross-template composition graph")
    lines.append("")
    lines.append(
        f"Graph of {len(templates)} templates and {len(edges)} inferred "
        "cross-template edges, derived from `template.json` descriptions, "
        "column notes, and action notes."
    )
    lines.append("")
    lines.append("```mermaid")
    lines.append("graph LR")
    # Nodes (alphabetical).
    for slug in sorted(templates.keys()):
        name = templates[slug]["template"].get("name", slug)
        nid = mermaid_id(slug)
        # Show slug above human name for scannability.
        label = f"{slug}<br/><i>{name}</i>"
        lines.append(f'    {nid}["{label}"]')
    lines.append("")
    # Edges.
    for f, t, lbl in edges:
        fid, tid = mermaid_id(f), mermaid_id(t)
        elabel = shorten(lbl, 40).replace('"', "'")
        lines.append(f'    {fid} -->|"{elabel}"| {tid}')
    lines.append("```")
    lines.append("")
    lines.append("## Edge legend")
    lines.append("")
    lines.append(
        "Edges are inferred from three signals: (1) a template's "
        "`template.description` or `notes` explicitly naming another template's "
        "slug; (2) phrases like `from upstream <X>` that imply a known class of "
        "upstream template; (3) phrases like `ready to hand to /clay-outbound` "
        "that imply a downstream cadence template."
    )
    lines.append("")
    lines.append("## Templates")
    lines.append("")
    lines.append("| Slug | Name | Use case | Motion |")
    lines.append("|------|------|----------|--------|")
    for slug in sorted(templates.keys()):
        t = templates[slug]["template"]
        lines.append(
            f"| [`{slug}`](per-template/{slug}.md) | {t.get('name','')} | "
            f"{t.get('use_case','')} | {t.get('motion','')} |"
        )
    lines.append("")
    return "\n".join(lines) + "\n"


def render_per_template(
    slug: str,
    tpl: dict,
    cross_edges: List[Tuple[str, str, str]],
) -> str:
    t = tpl["template"]
    col_edges = build_column_dag(tpl)

    feeds_into = sorted({to_s for f, to_s, _ in cross_edges if f == slug})
    fed_by = sorted({f for f, to_s, _ in cross_edges if to_s == slug})

    lines: List[str] = []
    lines.append(AUTOGEN_BANNER)
    lines.append("")
    lines.append(f"# {t.get('name', slug)}")
    lines.append("")
    lines.append(f"**Slug:** `{slug}`  ")
    lines.append(f"**Use case:** {t.get('use_case', '')}  ")
    lines.append(f"**Motion:** {t.get('motion', '')}  ")
    lines.append(f"**Cost/row:** {t.get('estimated_cost_per_row', '')}  ")
    lines.append(f"**Match rate:** {t.get('expected_match_rate', '')}")
    lines.append("")
    lines.append(t.get("description", "") or "")
    lines.append("")
    lines.append("## Internal column DAG")
    lines.append("")
    lines.append(
        f"{len(tpl.get('columns', []))} columns, {len(col_edges)} "
        "dependency edges (including action triggers)."
    )
    lines.append("")
    lines.append("```mermaid")
    lines.append("graph TD")
    # Nodes — declare all columns first.
    for col in tpl.get("columns", []):
        cname = col["name"]
        ctype = col.get("type", "")
        nid = mermaid_id(cname)
        lines.append(f'    {nid}["{cname}<br/><i>{ctype}</i>"]')
    # Action nodes.
    for act in tpl.get("actions", []):
        aid = mermaid_id(f"[action] {act['name']}")
        lines.append(f'    {aid}(["action: {act["name"]}"])')
    # Edges.
    for f, to_ in col_edges:
        fid = mermaid_id(f)
        tid = mermaid_id(to_)
        lines.append(f"    {fid} --> {tid}")
    lines.append("```")
    lines.append("")
    lines.append("## Cross-template links")
    lines.append("")
    if fed_by:
        lines.append("### Fed by")
        lines.append("")
        for s in fed_by:
            lines.append(f"- [`{s}`]({s}.md)")
        lines.append("")
    else:
        lines.append("### Fed by")
        lines.append("")
        lines.append("_None inferred. This template is a top-of-funnel source._")
        lines.append("")

    if feeds_into:
        lines.append("### Feeds into")
        lines.append("")
        for s in feeds_into:
            lines.append(f"- [`{s}`]({s}.md)")
        lines.append("")
    else:
        lines.append("### Feeds into")
        lines.append("")
        lines.append("_None inferred. This template is terminal._")
        lines.append("")
    return "\n".join(lines) + "\n"


# ---------- I/O / idempotency ----------


def planned_files(
    templates: Dict[str, dict],
    edges: List[Tuple[str, str, str]],
) -> Dict[Path, str]:
    out: Dict[Path, str] = {}
    out[DOCS_DIR / "library-graph.md"] = render_library_graph(templates, edges)
    for slug, tpl in templates.items():
        out[PER_TEMPLATE_DIR / f"{slug}.md"] = render_per_template(
            slug, tpl, edges
        )
    return out


def check_or_write(files: Dict[Path, str], check_only: bool) -> int:
    """If check_only: exit non-zero on any diff. Else: write all files."""
    if check_only:
        drift: List[Path] = []
        for path, content in files.items():
            if not path.exists():
                drift.append(path)
                continue
            on_disk = path.read_text()
            if on_disk != content:
                drift.append(path)
        if drift:
            print("compose-graph: drift detected in:", file=sys.stderr)
            for p in drift:
                print(f"  - {p.relative_to(REPO_ROOT)}", file=sys.stderr)
            return 1
        print("compose-graph: docs in sync.")
        return 0

    # Write mode.
    DOCS_DIR.mkdir(parents=True, exist_ok=True)
    PER_TEMPLATE_DIR.mkdir(parents=True, exist_ok=True)
    for path, content in files.items():
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
    print(f"compose-graph: wrote {len(files)} file(s) under {DOCS_DIR.relative_to(REPO_ROOT)}/")
    return 0


def main(argv: Iterable[str] | None = None) -> int:
    p = argparse.ArgumentParser(description=__doc__)
    p.add_argument(
        "--check",
        action="store_true",
        help="Exit non-zero if generated docs would differ from disk.",
    )
    args = p.parse_args(argv)

    if not LIBRARY_DIR.exists():
        print(f"error: {LIBRARY_DIR} not found", file=sys.stderr)
        return 2

    templates = load_templates()
    if not templates:
        print(f"error: no templates found under {LIBRARY_DIR}", file=sys.stderr)
        return 2

    edges = infer_cross_template_edges(templates)
    files = planned_files(templates, edges)
    return check_or_write(files, check_only=args.check)


if __name__ == "__main__":
    raise SystemExit(main())
